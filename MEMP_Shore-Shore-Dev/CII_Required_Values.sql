INSERT INTO CII_Required_Master
SELECT
    VesselTypeKey,
    CapacityMin,
    CapacityMax,
    2026 AS CalendarYear,

    /* Required CII */
    RequiredCII * (1 - (rf2026.ReductionFactor - rf2025.ReductionFactor)),

    /* Boundaries scale identically */
    Boundary_A  * (1 - (rf2026.ReductionFactor - rf2025.ReductionFactor)),
    Boundary_B  * (1 - (rf2026.ReductionFactor - rf2025.ReductionFactor)),
    Boundary_C  * (1 - (rf2026.ReductionFactor - rf2025.ReductionFactor)),
    Boundary_D  * (1 - (rf2026.ReductionFactor - rf2025.ReductionFactor))

FROM CII_Required_Master base
JOIN CII_Reduction_Factors rf2025 ON rf2025.CalendarYear = 2025
JOIN CII_Reduction_Factors rf2026 ON rf2026.CalendarYear = 2026
WHERE base.CalendarYear = 2025;
